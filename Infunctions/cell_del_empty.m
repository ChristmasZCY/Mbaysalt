function C = cell_del_empty(c)
    % =================================================================================================================
    % discription:
    %       delete empty cell in cell
    % =================================================================================================================
    % parameter:
    %       c: cell                          || required: True || type: cell   || format: {'key1','key2',''}
    %       C: cell                          || required: True || type: cell   || format: {'key1','key2'}
    % =================================================================================================================
    % example:
    %       C = cell_del_empty({'key1','key2',''})
    % =================================================================================================================

    C = c(~cellfun(@isempty, c));

end