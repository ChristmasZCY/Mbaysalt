function HOME = getHome()
    %       Get home path
    % =================================================================================================================
    % Parameter:
    %
    % =================================================================================================================
    % Returns:
    %       HOME:   Home path                || type: char || format: '/Users/christmas'
    % =================================================================================================================
    % Update:
    %       2024-05-30:     Created, by Christmas;
    % =================================================================================================================
    % Example:
    %       HOME = getHome();
    % =================================================================================================================

    switch checkOS
        case {'WIN'}
            HOME = getenv('UserProfile');
        case {'MAC', 'LNX'}
            HOME = getenv('HOME');
        otherwise
            error('Unidentification system !!!')
    end

end
